`timescale 1ns / 1ps

module instruction_mem (
    input  logic [31:0] instr_addr, // 입력: CPU(Datapath)에서 나오는 PC 주소
    output logic [31:0] instr_code  // 출력: 제어부 및 데이터패스로 들어갈 32비트 기계어
);

    // 하드웨어 내부에 32비트 크기의 메모리 방 16개(RAM 구조 배열)를 선언합니다.
    logic [31:0] rom_memory[0:15];

    // 시뮬레이션 시작 시점에 메모리 셀에 검증용 기계어(Hex 값)를 주입합니다.
    initial begin
        // [참고용 초기 레지스터 상태] x1=1, x2=2, x3=3, x4=4, x5=5, x6=6, x7=7, x12=12, x31=31
        
        // [PC: 0x00] SUB : x20 = x2 - x7 -> 2 - 7 = -5 (결과값: 32'hffff_fbfb)
        // 검증 포인트: 연산 결과가 음수일 때 2의 보수로 상위 부호 비트가 1로 채워지는가?
        rom_memory[0]  = 32'h4071_0a33;  

        // [PC: 0x04] SLTI : x21 = (x20 < 1) ? 1 : 0 -> -5 < 1 은 참이므로 '1' (결과값: 32'h0000_0001)
        // 검증 포인트: 부호가 있는 비교 연산(SLT)에서 음수가 양수보다 작다고 정확히 판정하는가?
        rom_memory[1]  = 32'h001a_2a93;  

        // [PC: 0x08] SLTIU : x22 = (x20 < 1) ? 1 : 0 -> 무부호 비교이므로 거짓 '0' (결과값: 32'h0000_0000)
        // 검증 포인트: 부호가 없는 비교(SLTU)에서 음수(-5)를 32'hffff_fbfb라는 아주 큰 양수로 해석하는가?
        rom_memory[2]  = 32'h001a_3b13;  

        // [PC: 0x0C] SRA : x23 = x20 >>> x2 -> -5 >>> 2 (결과값: 32'hffff_fffe, 즉 -2)
        // 검증 포인트: 음수를 오른쪽으로 밀 때 부호 비트(1)가 복사되면서 음수 상태를 유지하는가?
        rom_memory[3]  = 32'h402a_5b33;  

        // [PC: 0x10] SRL : x24 = x20 >> x2  -> -5 >> 2 (결과값: 32'h3fff_fffe)
        // 검증 포인트: SRA와 달리 SRL은 음수를 밀 때 왼쪽에 무조건 0이 채워져 양수화되는가?
        rom_memory[4]  = 32'h002a_5c33;  

        // [PC: 0x14] ADD : x25 = x31 + x31 -> 31 + 31 = 62 (결과값: 32'h0000_003e)
        // 검증 포인트: 가산기(Adder)가 경계 조건에 가까운 양수 최대값 합산을 비트 유실 없이 처리하는가?
        rom_memory[5]  = 32'h01ff_0cb3;  

        // [PC: 0x18] ADD : x0 = x1 + x2 -> 1 + 2 = 3 이나 x0이므로 결과값은 무조건 '0' 고정
        // 검증 포인트: 하드웨어적으로 레지스터 x0에 데이터 쓰기 요청이 들어왔을 때 무시하고 0을 유지하는가?
        rom_memory[6]  = 32'h0020_8033;  

        // [PC: 0x1C] ADD : x26 = x0 + x0 -> 0 + 0 = 0 (결과값: 32'h0000_0000)
        // 검증 포인트: x0 레지스터를 소스(rs1, rs2)로 읽어올 때 상시 0으로 올바르게 청독되는가?
        rom_memory[7]  = 32'h0000_0d33;  

        // [PC: 0x20] SUB : x27 = x12 - x12 -> 12 - 12 = 0 (결과값: 32'h0000_0000)
        // 검증 포인트: 동일한 값 감산 시 데이터 라인과 제로 플래그 꼬임 없이 깨끗하게 0으로 정렬되는가?
        rom_memory[8]  = 32'h40c6_0e33;  

        // [PC: 0x24] SLL : x28 = x1 << x31 -> 1 << 31 (결과값: 32'h8000_0000)
        // 검증 포인트: 왼쪽 시프트 시 데이터 1을 최상위 비트(MSB) 바운더리 끝까지 완전히 밀어낼 수 있는가?
        rom_memory[9]  = 32'h01f0_9e33;  

        // 나머지 사용하지 않는 주소 영역(PC: 0x28 ~ 0x3C)은 0으로 안전하게 패딩합니다.
        rom_memory[10] = 32'h0000_0000;
        rom_memory[11] = 32'h0000_0000;
        rom_memory[12] = 32'h0000_0000;
        rom_memory[13] = 32'h0000_0000;
        rom_memory[14] = 32'h0000_0000;
        rom_memory[15] = 32'h0000_0000;
    end

    // 바이트 단위 주소(PC)를 4로 나누어(하위 2비트 절삭) 내부 32비트 배열의 인덱스로 매핑합니다.
    // 조합 회로로 구성되어 주소가 들어오는 즉시 지연 없이 명령어를 방출합니다.
    assign instr_code = rom_memory[instr_addr[31:2]];

endmodule